package ex_04_Operators;

public class Lab041_Interview_Concat_Plus {
    public static void main(String[] args) {
        //Concatenation
         String s1 = "Aishwarya";
         String s2 = "Shingare";
        System.out.println(s1 + s2); // + operator performs joining string

        int a = 10;
        int b = 20;
        System.out.println(a + b); // + operator performs maths operation

        // + -> behave differently with the different data type
        // + -> operator overloading


    }
}
