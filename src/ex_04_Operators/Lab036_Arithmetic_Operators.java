package ex_04_Operators;

public class Lab036_Arithmetic_Operators {
    public static void main(String[] args) {
        //Arithmetic Operators
        // + -> Addition
        // - -> subtraction
        // * -> Multiplication
        // / -> Division
        // % -> Modulus (Remainder)

        int a = 20;
        int b = 3;
        float c = 3.0f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        System.out.println(a/c); // o/p is in float
        System.out.println("a+b"); //o/p is a+b
    }
}
