package ex_10_For_Loop;

public class Lab103_For_If_Loop {
    public static void main(String[] args) {
        for (int shruti=0; shruti < 18; shruti++){
            if (shruti > 15){
                System.out.println("Gift from papa");
            }
            else {
                System.out.println("No gift");
            }
        }
    }
}
