package Tasks.Task34_Loops_Programs.While_Loop;

public class Printing_Reverse_Number {
    public static void main(String[] args) {
        int i = 10;

        System.out.println("Numbers from 1 to 10 in reverse order: ");

        while (i >= 1){
            System.out.println(i);
            i--;
        }
    }
}
